    <div class="rules-section">
        <h2>Core Rules</h2>
        <div class="rule-category">
            <h3>Survivor Progression</h3>
            <ul>
                <li>Each survivor starts with 3 lives</li>
                <li>You must play a survivor 6 times to unlock their perks for all survivors</li>
                <li>General Perks are available to all survivors from the start</li>
                <li>Survivors are eliminated when they lose their last life</li>
            </ul>
        </div>

        <div class="rule-category">
            <h3>Revival System</h3>
            <ul>
                <li>You can only revive a survivor once in their lifetime</li>
                <li>You can only revive one survivor per rank</li>
                <li>A revived survivor's rent is doubled</li>
                <li>Revival costs 50% of the survivor's rental price</li>
            </ul>
        </div>

        <div class="rule-category">
            <h3>Loadout Restrictions</h3>
            <ul>
                <li>You can only use the same perk 3 times in a row</li>
                <li>After using a perk 3 times, it goes on a 2-match cooldown</li>
                <li>Extreme Hardcore Mode reduces lives to 1 but doubles earnings</li>
                <li>Each survivor can only use their own perks until unlocked</li>
                <li>If tunnel insurance is used, the survivor doesn't lose a life but is benched for 3 matches</li>
                <li>Empty perk slots do not count towards perk restrictions - only equipped perks are considered</li>
            </ul>
        </div>

        <div class="rule-category">
            <h3>Economy Rules</h3>
            <ul>
                <li>If bankrupt, survivors can be sold for half their rental value and are permanently eliminated</li>
                <li>Each match costs the sum of your loadout items</li>
                <li>Earnings are based on match performance</li>
                <li>Bankruptcy occurs when you can't afford your next match</li>
            </ul>
        </div>
    </div>

